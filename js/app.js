function showNamazTime(city) {
   let counter = 0;
   let name = ['Fajr', 'Dhuhr', 'Asr', 'Maghrib', 'Isha'];
   $('#prayerTimes').html('');

   $.getJSON(`https://muslimsalat.com/${city}/daily.json/?key=92d2a5ee0edd04dff0958008adaebb1f&jsoncallback=?`,
      function (times) {

         for (let i = 0; i < name.length; i++) {
            $('#prayerTimes').append(`
            <tr>
               <th>${counter + 1}</th>
               <td>${name[counter]}</td>
               <td>${getNamazTime(counter, times)}</td>
            </tr>
         `);
            counter++;
         }

      });

   const getNamazTime = (namaz, times) => {
      switch (namaz) {
         case 0: return times.items[0].fajr;
         case 1: return times.items[0].dhuhr;
         case 2: return times.items[0].asr;
         case 3: return times.items[0].maghrib;
         case 4: return times.items[0].isha;
      }

   }
}

